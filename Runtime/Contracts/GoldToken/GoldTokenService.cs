        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Nethereum.Web3.Web3 web3, GoldTokenDeployment goldTokenDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<GoldTokenDeployment>().SendRequestAndWaitForReceiptAsync(goldTokenDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.Web3 web3, GoldTokenDeployment goldTokenDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<GoldTokenDeployment>().SendRequestAsync(goldTokenDeployment);
        }

        public static async Task<GoldTokenService> DeployContractAndGetServiceAsync(Nethereum.Web3.Web3 web3, GoldTokenDeployment goldTokenDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, goldTokenDeployment, cancellationTokenSource);
            return new GoldTokenService(web3, receipt.ContractAddress);
        }

        protected Nethereum.Web3.Web3 Web3{ get; }

