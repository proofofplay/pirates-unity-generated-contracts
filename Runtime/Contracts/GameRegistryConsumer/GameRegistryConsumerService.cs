        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Nethereum.Web3.Web3 web3, GameRegistryConsumerDeployment gameRegistryConsumerDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<GameRegistryConsumerDeployment>().SendRequestAndWaitForReceiptAsync(gameRegistryConsumerDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.Web3 web3, GameRegistryConsumerDeployment gameRegistryConsumerDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<GameRegistryConsumerDeployment>().SendRequestAsync(gameRegistryConsumerDeployment);
        }

        public static async Task<GameRegistryConsumerService> DeployContractAndGetServiceAsync(Nethereum.Web3.Web3 web3, GameRegistryConsumerDeployment gameRegistryConsumerDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, gameRegistryConsumerDeployment, cancellationTokenSource);
            return new GameRegistryConsumerService(web3, receipt.ContractAddress);
        }

        protected Nethereum.Web3.Web3 Web3{ get; }

        public ContractHandler ContractHandler { get; }

        public GameRegistryConsumerService(Nethereum.Web3.Web3 web3, string contractAddress)
        {
            Web3 = web3;
            ContractHandler = web3.Eth.GetContractHandler(contractAddress);
        }

